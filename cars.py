#the sort() Method
cars = ['bmw', 'audi', 'toyota', 'subaru' ]
cars.sort(reverse=True)
print(cars)

#Sorting a List Temporarily with the sorted() Function
cars = ['bmw', 'audi', 'toyota', 'subaru' ]
print("Here is original list:")   
print(cars)

print("\nHere's the sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)

